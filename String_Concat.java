package String_Practics;

//  Write a program in string using concat method()
// concat = add karna
 public class
String_Concat{
    public static void main(String[] args){

        String FirstName = " Praveen Ranjan";
        String SecondName = " Ankit Ojha ";

        System.out.println(SecondName.concat(FirstName));
        System.out.println(SecondName.trim());
        System.out.println(FirstName + SecondName);

    }
}
